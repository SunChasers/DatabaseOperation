$(document).ready(function() {
/*左侧选择栏鼠标移动*/
	$("#left_li01").mousemove(function() {
		$("#left_li01").css("background-color", "rgb(228,228,228)");
	});
	$("#left_li01").mouseout(function() {
		$("#left_li01").css("background-color", "#FFFFFF");
	});
	$("#left_li02").mousemove(function() {
		$("#left_li02").css("background-color", "rgb(228,228,228)");
	});
	$("#left_li02").mouseout(function() {
		$("#left_li02").css("background-color", "#FFFFFF");
	});
	$("#left_li03").mousemove(function() {
		$("#left_li03").css("background-color", "rgb(228,228,228)");
	});
	$("#left_li03").mouseout(function() {
		$("#left_li03").css("background-color", "#FFFFFF");
	});
	
/*左侧选择栏点击事件*/
	$("#left_li01").click(function() {
		$(".right1").css("display", "");
		$(".right2").css("display", "none");
		
	});
	$("#left_li02").click(function() {
		$(".right1").css("display", "none");
		$(".right2").css("display", "");
	});
	$("#left_li03").click(function() {
		window:location='WebPage/jsp/forums_tab3.jsp';
	});
});